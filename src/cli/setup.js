// Interactive wizard
// ---------------------------------------------------------------------------

async function runInteractive(env) {
  const rl = createRl();
  const cfg = {};

  // 1. Welcome
  outln();
  outln('========================================');
  outln('  Knowledge Base Server Setup');
  outln('========================================');
  outln();

  // 2. Environment
  outln('  Detected environment:');
  outln(`    OS:   ${env.os}`);
  outln(`    Node: ${env.nodeVersion}`);
  outln(`    AI tools found:`);
  for (const [, info] of Object.entries(env.tools)) {
    outln(`      ${info.available ? '[x]' : '[ ]'} ${info.label}`);
  }
  outln();

  // 3. Dashboard password
  const randomPw = genHex(8);
  outln('  Dashboard password protects the web UI.');
  outln('  Press Enter to generate a random one, or type your own.');
  cfg.password = await ask(rl, 'Dashboard password', randomPw);

  // 4. Server port
  outln();
  outln('  The dashboard and API will listen on this port.');
  const portStr = await ask(rl, 'Server port', '3838');
  cfg.port = parseInt(portStr, 10) || 3838;

  // 5. Obsidian vault
  outln();
  outln('  If you use Obsidian, the vault will be indexed for search.');
  const detected = detectVaultPath();
  const vaultDefault = detected || 'none';
  if (detected) {
    outln(`  Auto-detected vault: ${detected}`);
  }
  outln('  Enter a path, or "none" to skip Obsidian integration.');
  const vaultAnswer = await ask(rl, 'Obsidian vault path', vaultDefault);
  cfg.vaultPath = vaultAnswer === 'none' ? '' : resolve(vaultAnswer.replace(/^~/, HOME));

  // 6. AI agents
  outln();
  outln('  Select which AI agents will connect to the knowledge base.');
  outln('  Each gets a unique API key for authentication.');
  const agentChoices = ['Claude Code', 'OpenAI Codex', 'Google Gemini', 'Ollama'];
  const agentKeys = ['claude', 'codex', 'gemini', 'ollama'];
  const selected = await askMulti(rl, 'Select agents (comma-separated numbers, or A for all)', agentChoices);
  cfg.agents = selected.map(i => agentKeys[i]);
  cfg.apiKeys = {};
  for (const agent of cfg.agents) {
    cfg.apiKeys[agent] = genHex();
  }

  // 7. Deployment mode
  const deployChoices = [];
  const deployKeys = [];
  if (platform() === 'linux') { deployChoices.push('systemd (auto-start on boot)'); deployKeys.push('systemd'); }
  if (platform() === 'darwin') { deployChoices.push('launchd (auto-start on boot)'); deployKeys.push('launchd'); }
  deployChoices.push('Docker (docker-compose)');
  deployKeys.push('docker');
  deployChoices.push('PM2 (process manager)');
  deployKeys.push('pm2');
  deployChoices.push('Manual (run kb start yourself)');
  deployKeys.push('manual');

  outln();
  outln('  How should the server run?');
  const deployIdx = await askChoice(rl, 'Deployment mode', deployChoices, 0);
  cfg.deploy = deployKeys[deployIdx];

  // 8. Brain API
  outln();
  outln('  The Brain API allows remote HTTPS access from AI agents.');
  outln('  Requires a domain name and reverse proxy (e.g., Caddy, nginx).');
  const brainAnswer = await ask(rl, 'Enable Brain API? (y/N)', 'n');
  cfg.brainApi = brainAnswer.toLowerCase().startsWith('y');
  if (cfg.brainApi) {
    cfg.brainDomain = await ask(rl, 'Domain for Brain API', 'brain.yourdomain.com');
  }

  cfg.authSecret = genBase64();

  rl.close();
  return cfg;
}

// ---------------------------------------------------------------------------
// Apply configuration
// ---------------------------------------------------------------------------

function applyConfig(cfg) {
  const results = { steps: [] };

  // 1. Write .env
  const envContent = buildEnvContent(cfg);
  const envPath = ENV_PATH;
  const envExisted = existsSync(envPath);
  writeFileSync(envPath, envContent);
  results.steps.push({
    action: envExisted ? 'Updated .env' : 'Created .env',
    path: envPath,
  });

  // 2. Register MCP for each agent that has a registrar
  for (const agent of (cfg.agents || [])) {
    if (MCP_REGISTRARS[agent]) {
      try {
        const path = MCP_REGISTRARS[agent]();
        results.steps.push({ action: `Registered MCP for ${agent}`, path });
      } catch (err) {
        results.steps.push({ action: `Failed to register MCP for ${agent}`, error: err.message });
      }
    }
  }

  // 3. Install service
  // Warn npx users about persistent service limitations
  if (isNpx() && cfg.deploy !== 'manual' && cfg.deploy !== 'docker') {
    results.steps.push({
      action: 'Warning: npx detected',
      hint: 'Persistent services (systemd/launchd/PM2) require a global install: npm i -g knowledge-base-server. The npx cache is ephemeral.',
    });
  }

  if (cfg.deploy === 'systemd') {
    const r = installSystemd();
    if (r.ok) {
      results.steps.push({ action: 'Installed systemd service', path: r.path });
    } else {
      results.steps.push({
        action: 'Could not install systemd service (may need sudo)',
        error: r.error,
        hint: 'Save the unit file manually and run: sudo systemctl daemon-reload && sudo systemctl enable knowledge-base',
      });
    }
  } else if (cfg.deploy === 'launchd') {
    const r = installLaunchd();
    if (r.ok) {
      results.steps.push({ action: 'Installed launchd plist', path: r.path });
    } else {
      results.steps.push({ action: 'Could not install launchd plist', error: r.error });
    }
  } else if (cfg.deploy === 'docker') {
    const path = generateDockerCompose(cfg);
    results.steps.push({ action: 'Generated docker-compose.yml', path });
  } else if (cfg.deploy === 'pm2') {
    results.steps.push({
      action: 'PM2 selected',
      hint: 'Run: pm2 start $(which kb) --name knowledge-base -- start',
    });
  }

  // 4. First ingest if vault provided
  if (cfg.vaultPath && existsSync(cfg.vaultPath)) {
    results.ingestPath = cfg.vaultPath;
  }

  results.cfg = cfg;
  return results;
}

// ---------------------------------------------------------------------------
// Print summary
// ---------------------------------------------------------------------------

function printSummary(results) {
  outln();
  outln('========================================');
  outln('  Setup Complete');
  outln('========================================');
  outln();

  for (const step of results.steps) {
    outln(`  [done] ${step.action}`);
    if (step.path) outln(`         ${step.path}`);
    if (step.hint) outln(`         ${step.hint}`);
    if (step.error) outln(`         Error: ${step.error}`);
  }

  const cfg = results.cfg;
  outln();
  outln('  SENSITIVE — do not share or log the following values:');
  outln('  Configuration summary:');
  outln(`    Port:      ${cfg.port}`);
  outln(`    Password:  ${cfg.password}`);
  outln(`    Vault:     ${cfg.vaultPath || '(none)'}`);
  outln(`    Agents:    ${(cfg.agents || []).join(', ') || '(none)'}`);
  outln(`    Deploy:    ${cfg.deploy || 'manual'}`);
  if (cfg.brainApi) {
    outln(`    Brain API: https://${cfg.brainDomain}`);
  }

  if (cfg.apiKeys && Object.keys(cfg.apiKeys).length > 0) {
    outln();
    outln('  SENSITIVE — do not share or log the following values:');
    outln('  API keys (save these — they are not stored elsewhere):');
    for (const [agent, key] of Object.entries(cfg.apiKeys)) {
      outln(`    ${agent}: ${key}`);
    }
  }

  outln();
  outln('  Next steps:');
  outln('    1. Review .env and adjust if needed');
  if (results.ingestPath) {
    outln(`    2. Run: kb ingest ${results.ingestPath}`);
    outln('    3. Run: kb start');
  } else {
    outln('    2. Run: kb start');
  }
  outln(`    Dashboard: http://localhost:${cfg.port}`);
  outln();
}

// ---------------------------------------------------------------------------
// Main entry point
// ---------------------------------------------------------------------------

export async function setup(args = []) {
  const isAuto = args.includes('--auto');

  const env = detectEnvironment();

  if (isAuto) {
    // Agent mode: combine config file + CLI flags, no prompts
    const fileConfig = loadConfigFile() || {};
    const cliConfig = parseAutoArgs(args);
    const merged = { ...fileConfig, ...cliConfig };

    // Apply defaults for anything not specified
    const cfg = {
      port: merged.port || 3838,
      password: merged.password || genHex(8),
      vaultPath: merged.vaultPath !== undefined ? merged.vaultPath : (detectVaultPath() || ''),
      agents: merged.agents || Object.keys(env.tools).filter(k => env.tools[k].available),
      deploy: merged.deploy || 'manual',
      brainApi: merged.brainApi || false,
      brainDomain: merged.brainDomain || 'brain.yourdomain.com',
      authSecret: merged.authSecret || genBase64(),
      apiKeys: {},
    };

    // Generate API keys for each agent
    for (const agent of cfg.agents) {
      cfg.apiKeys[agent] = merged.apiKeys?.[agent] || genHex();
    }

    outln('Knowledge Base Server — automatic setup');
    outln();

    const results = applyConfig(cfg);
    printSummary(results);
    return;
  }

  // Interactive mode
  const cfg = await runInteractive(env);
  const results = applyConfig(cfg);
  printSummary(results);
}
