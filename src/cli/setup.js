function installSystemd() {
  // Resolve kb binary path — use the actual resolved path, not just 'kb',
  // because systemd PATH may not include npm bin directories
  let kbBin;
  try {
    kbBin = execFileSync('which', ['kb'], { encoding: 'utf-8' }).trim();
  } catch {
    kbBin = 'kb'; // fallback — user will need to fix PATH in unit
  }
  const unit = `[Unit]
Description=Knowledge Base Server
After=network.target

[Service]
Type=simple
User=${process.env.USER || 'root'}
WorkingDirectory=${HOME}
ExecStart=${kbBin} start
Restart=on-failure
RestartSec=5
Environment=NODE_ENV=production

[Install]
WantedBy=multi-user.target
`;
  const unitPath = '/etc/systemd/system/knowledge-base.service';
  try {
    writeFileSync(unitPath, unit);
    execFileSync('systemctl', ['daemon-reload'], { stdio: 'ignore' });
    execFileSync('systemctl', ['enable', 'knowledge-base'], { stdio: 'ignore' });
    return { ok: true, path: unitPath };
  } catch (err) {
    return { ok: false, error: err.message, content: unit };
  }
}

function installLaunchd() {
  let kbBin;
  try {
    kbBin = execFileSync('which', ['kb'], { encoding: 'utf-8' }).trim();
  } catch {
    kbBin = '/usr/local/bin/kb';
  }
  const plist = `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
  <key>Label</key>
  <string>com.knowledgebase.server</string>
  <key>ProgramArguments</key>
  <array>
    <string>${kbBin}</string>
    <string>start</string>
  </array>
  <key>WorkingDirectory</key>
  <string>${HOME}</string>
  <key>RunAtLoad</key>
  <true/>
  <key>KeepAlive</key>
  <true/>
</dict>
</plist>
`;
  const plistPath = join(HOME, 'Library', 'LaunchAgents', 'com.knowledgebase.server.plist');
  try {
    mkdirSync(join(HOME, 'Library', 'LaunchAgents'), { recursive: true });
    writeFileSync(plistPath, plist);
    return { ok: true, path: plistPath };
  } catch (err) {
    return { ok: false, error: err.message, content: plist };
  }
}

function generateDockerCompose(cfg) {
  const content = `version: "3.8"
services:
  knowledge-base:
    image: node:22-slim
    command: ["npx", "knowledge-base-server", "start"]
    ports:
      - "${cfg.port}:${cfg.port}"
    volumes:
      - kb-data:/root/.knowledge-base
${cfg.vaultPath ? `      - ${cfg.vaultPath}:/vault` : ''}
    environment:
      - NODE_ENV=production
    restart: unless-stopped

volumes:
  kb-data:
`;
  const composePath = join(KB_DIR, 'docker-compose.yml');
  writeFileSync(composePath, content);
  return composePath;
}

